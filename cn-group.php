                        <h2>Výsledek</h2>
                        <div class="opacity-box">
                      <p>Kompletní redesign webových stránek společnosti CN Group se mi podařilo <strong>realizovat během necelých čtyřech měsíců</strong>. Úzce jsem přitom kooperoval s interními zaměstnanci z vývojového a marketingového oddělení. V průběhu několika dalších týdnů jsem pomocí <strong>analytických nástrojů sledoval chování uživatelů</strong> na webu a odstraňoval drobné nedostatky.</p> 

                      <p>Podle následně získaných ohlasů od zaměstnanců a zákazníků společnosti CN Group byl nový vzhled, funkčnost a obsahová stránka webu <strong>hodnoceny velmi pozitivně</strong>.</p>
