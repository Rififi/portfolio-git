                        <div class="project-single-title">
                            <span class="small-title opacity-box">Případová studie</span>
                            <h1>NIVEKO Pools – redesign firemního webu</h1>
                        </div>
                        <div class="project-single-desc">
                            <p>Společnost NIVEKO se zabývá <strong>výrobou bazénů na míru</strong>. Během více než 20 let na trhu si firma vysloužila <strong>vysoké renomé u zákazníků z celé Evropy</strong>. O kvalitách bazénů NIVEKO se každý rok přesvědčí více než 450 spokojených klientů. Díky spojení unikátního designu s nejnovějšími technologiemi se jméno <strong>NIVEKO stalo synonymem pro kvalitní bazén bez kompromisů</strong>.</p>
                        </div>
                    </div>
                    <div class="margin-box margin-p">
                        <h2>Problém</h2>
                        <div class="opacity-box">
                            <p>V roce 2016 jsem začal spolupracovat s agenturou <strong>Digital Brain</strong> jako kontraktor na pozici <strong>senior UX designer</strong>. Jedním z úkolů, které jsem s agenturou Digital Brain řešil, byl <strong>kompletní redesign firemního webu společnosti NIVEKO</strong>.</p>  

                            <p><strong>Stávající web již firmě NIVEKO nevyhovoval po obsahové</strong>, <strong>technické</strong> ani <strong>vizuální stránce</strong>. Nepřehledná navigační struktura, nejednotná corporátní identita, nedostatečná responsivita, komplikovaný bazénový konfigurátor nebo příliš mnoho jazykových mutací byly jen některé z hlavních problémů, které si žádaly v rámci redesignu odborné řešení.</p>                  
                        </div>
                    </div>
                    <div class="margin-box">
                        <h2>Postup řešení a UX metody</h2>
                        <div class="opacity-box">
                <ul>
              <li>Podílel jsem se na <strong>workshopu s vedením společnosti</strong> a marketingovým oddělením, na kterém byly definovány hlavní cíle redesignu</li>
              <li>Na základě rozhovorů s klientem jsem vymezil <strong>hlavní persony</strong>, na který by měl být nový web zaměřen
               <ul>
                   <li><strong>Zahraniční zákazníci</strong> ze západní a střední Evropy a zejména z německy mluvící zemí</li>
                   <li><strong>Prodejci a obchodní partneři</strong> společnosti NIVEKO</li>
               </ul>    
              </li>
              <li>Ve spolupráci s copywriterem jsem se podílel na přípravě <strong>obsahové strategie</strong></li>
              <li>Zpřehlednil jsem informační architekturu
                <ul>
                    <li>Po konzultacích s obchodním oddělením jsem prosadil <strong>zachování pouze tří dominantních jazykových mutací</strong> (CZ, EN, DE)</li>
                    <li>Zjednodušil jsem počet prvků v navigaci</li>
                    <li>Odstranil jsem zbytečné kroky v <strong>bazénovém konfigurátoru</strong></li>
                    <li>Zdůraznil jsem <strong>vizuální význam referencí</strong></li>
                    <li><strong>Zpřehlednil jsem sekci prodejců pomocí interaktivní mapy</strong> a jednoduchého členění dle států</li>
                </ul>
              </li>
              <li>Vytvořil jsem <strong>několik sad wireframů</strong> a <strong>interaktivních prototypů</strong>, které jsem otestoval a konzultoval s marketingovým a obchodním oddělením</li>
              <li>Několik týdnů od spuštění nových webových stránek jsem <strong>sledoval chování uživatelů pomocí analytických a trackovacích nástrojů</strong> a na základě získaných výsledků jsem provedl několik menších korekcí</li>

 </ul>

 </div>
