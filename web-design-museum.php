                        <div class="project-single-title">
                            <span class="small-title opacity-box">Případová studie</span>
                            <h1>Web Design Museum – první museum webového designu na světě</h1>
                        </div>
                        <div class="project-single-desc">
                            <p><strong>Web Design Museum vystavuje více než 800 unikátních designů webových stránek</strong>, které byly vytvořeny v letech 1995 až 2005. Museum mapuje zapomenuté trendy webdesignu a na vybraných webových stránkách zobrazuje jejich vývoj od nejstarší podoby až do současnosti.</p> 
                       </div>
                    </div>
                    <div class="margin-box margin-p">
                        <h2>Problém</h2>
                        <div class="opacity-box">
                            <p>První webová stránka na světě byla spuštěna 6. srpna 1991. Její originál se bohužel do dnešních dnů nezachoval a je známá pouze její kopie z roku 1992. <strong>Většina webových stránek, které vznikly do roku 1996 nenávratně zmizela v hlubinách času</strong> a jejich podobnu neznáme. Od roku 1996 existuje ale digitální knihovna <strong>Internet Archive</strong>, která pomocí služby <strong>Wayback Machine</strong> archivuje obsah a vzhled většiny internetových stránek.</p>
                            <p>
                              Internet Archive však webové stránky pouze mechanicky ukládá a nesnaží se návštěvníkům předkládat ucelený obraz z minulosti webového designu. <strong>Web Design Museum si proto klade za hlavní cíl:</strong> zmapovat zaniklé trendy ve webovém designu, zpřístupnit široké veřejnosti pomocí vybraných exponátů komplexní pohled na minulost webdesignu a ukázat na vybraných webech jejich vývoj od nejstarší minulosti až do současnosti.  
                            </p>
                        </div>
                    </div>
                    <div class="margin-box">
                        <h2>Postup řešení a UX metody</h2>
                        <div class="opacity-box">
                           <ul>
                               <li>Vytvořil jsem <strong>koncepci prvního musea webového designu na světě</strong></li>
                               <li>Provedl jsem <strong>guerillový uživatelský výzkum</strong> a ověřil si, že vize musea webového designu je životaschopná a pro uživatele přínosná</li>
                               <li>Definoval jsem <strong>klíčové persony</strong>
                           <ul>
                                    <li>Weboví designeři z <strong>první generace tvůrců webu</strong>, kteří si zapomenuté trendy webdesignu osobně pamatují</li>
                                    <li>Weboví designeři z celého světa, kteří se začali tvorbě webů věnovat až po roce 2005 a neví, jak vypadaly webové stránky například kolem roku 2000</li>
                                    <li>Široká veřejnost z celého světa se zájmem prohlédnout si vývoj vybraných webových stránek od jejich nejstarší podoby po současnost</li>
                                    <li>Historici a knihovníci zabývající se digitální archeologií</li>
                                </ul> 
                               </li>
                               <li>Připravil jsem <strong>informační architekturu</strong> s důrazem na jednoduchou a intuitivní navigaci</li>
                               <li>Vytvořil jsem <strong>několik sad wireframů</strong> a guerillovým způsobem je několikrát otestoval, abych si ověřil svoje hypotézy</li>
                               <li>Navrhl jsem <strong>kompletní grafický vzhled</strong> webových stránek a administrační rozhraní</li>
                               <li>Převedl jsem grafické návrhy do podoby <strong>HTML šablon</strong> za použití webového frameworku Google Material Design Lite</li>
                               <li>Nastavil jsem <strong>webovou analytiku</strong>
                                <ul>
                                    <li>Nadefinoval jsem sledování klíčových událostí v <strong>Google Analytics</strong></li>
                                    <li>Sledoval jsem a vyhodnocoval chování uživatelů pomocí <strong>trackovacího nástroje Smartlook</strong></li>
                                </ul>
                               </li>
                        </ul>
                        </div>
