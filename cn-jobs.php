                        <div class="project-single-title">
                            <span class="small-title opacity-box">Případová studie</span>
                            <h1>CN Jobs – návrh náborové microsite</h1>
                        </div>
                        <div class="project-single-desc">
                            <p><strong>CN Group je dánská vývojářská společnost</strong>, která působí na českém trhu od roku 1994. Firma se zabývá vývojem a údržbou náročných softwarových řešení <strong>na bázi nearshoringu</strong>. Mezi její hlavní klientelu patří zákazníci ze Skandinávie, Německa, Rakouska, Švýcarska nebo Velké Británie. I přes narůstající počet zaměstnanců, v současné době více než 200, si společnost <strong>snaží udržet vnitřní přátelskou</strong> a <strong>rodinnou atmosféru</strong>.
                            </p>
                        </div>
                    </div>
                    <div class="margin-box margin-p">
                        <h2>Problém</h2>
                        <div class="opacity-box">
                            <p>Podobně jako většina firem v oboru IT, tak i <strong>HR oddělení CN Group zápasí s dlouhodobým nedostatkem kvalitních vývojářů</strong>. Na počátku roku 2015 jsem začal s firmou CN Group spolupracovat jako <strong>kontraktor na pozici UX &amp; UI designer</strong>. V následujících měsících mě oslovilo marketingové a HR oddělení <strong>s nabídkou tvorby náborové microsite</strong>.</p> 

                            <p>Hlavním úkolem microsite bylo představení společnosti <strong>CN Group jako stabilní firmy</strong>, kde vládne <strong>neformální přátelská atmosféra</strong>, <strong>s možností osobního odborného růstu</strong> a šancí pracovat na projektech, které mají pro zahraniční klienty skutečný smysl a obchodní přínos.</p>               
                        </div>
                    </div>
                    <div class="margin-box">
                        <h2>Postup řešení a UX metody</h2>
                        <div class="opacity-box">
                <ul>
                    <li>Podílel jsem se na <strong>workshopu s marketingovým</strong> a <strong>HR oddělením</strong> s cílem definovat strategii a cíle, které by měla náborová microsite splňovat</li>
                    <li>Vedl jsem <strong>rozhovory s HR managery</strong>, abych lépe pochopil problémy, které se během náborového procesu často objevují</li>
                    <li>Provedl jsem několik <strong>hloubkových rozhovorů</strong> s čerstvě přijatými zaměstnanci, se záměrem zjistit, proč si zvolili nové zaměstnání právě v CN Group a zda nepostrádali nějaké důležité informace v průběhu přijímacího řízení</li>
                    <li>Navrhl jsem kompletní <strong>kreativní strategii náborové microsite</strong>, která hravou formou vyjadřovala přátelskou a rodinnou atmosféru, na které si společnosti CN Group zakládá pod heslem „<strong>Play hard, work hard</strong>“</li>
                    <li>Definoval jsem <strong>hlavní persony</strong>
                    <ul>
                    <li>Uchazeč o zaměstnání <strong>junior vývojář</strong></li> 
                    <li>Uchazeč o zaměstnání <strong>senior vývojář</strong></li> 
                    <li>Uchazeč o studium <strong>v programu CN University</strong></li>  
                    </ul>    
                    </li>
                <li>Připravil jsem jednoduchou a <strong>srozumitelnou informační architekturu</strong></li>
                <li>Ve spolupráci s HR oddělením jsem se <strong>podílel na psaní textů</strong></li>
                <li>Navrhl jsem <strong>sadu wireframů</strong> a otestoval je na nově přijatých zaměstnancích a studentech programu CN University</li>
                <li>Vytvořil jsem <strong>kreativní grafické návrhy</strong> v duchu kampaně „<strong>Nastartuj kariéru v CN Group a staň se naším novým superhrdinou!</strong>“</li>
                 </ul>

