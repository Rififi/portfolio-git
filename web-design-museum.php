                        <h2>Výsledek</h2>
                        <div class="opacity-box">
                      <p>Několik týdnů po svém spuštění <strong>zaznamenal projekt Web Design Museum výrazný celosvětový úspěch</strong>. Během prvních třech měsíců provozu navštívilo museum <strong>několik desítek tisíc návštěvníků</strong>. Články o projektu vyšly na mnoha zahraničních portálech a odkazy na museum a jeho exponáty se šířily ve velké míře spontánně přes sociální sítě.</p>
                      <p>Na začátku června 2017 pak obsadil projekt Web Design Museum na základě hlasování návštěvníků <strong>první místo na portále Reddit v sekci Design</strong>.</p>
