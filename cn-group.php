                            <span class="small-title opacity-box">Případová studie</span>
                            <h1>CN Group – redesign firemního webu</h1>
                        </div>
                        <div class="project-single-desc">
                            <p><strong>CN Group je dánská vývojářská společnost</strong>, která působí na českém trhu od roku 1994. Firma se zabývá vývojem a údržbou náročných softwarových řešení <strong>na bázi nearshoringu</strong>. Mezi její hlavní klientelu patří zákazníci ze Skandinávie, Německa, Rakouska, Švýcarska nebo Velké Británie. Společnost CN Group se rovněž věnuje konzultacím v oblasti IT managementu a QA testování.</p>
                        </div>
                    </div>
                    <div class="margin-box margin-p">
                        <h2>Problém</h2>
                        <div class="opacity-box">
                            <p>V lednu 2015 jsem začal spolupracovat se společností CN Group jako kontraktor na pozici <strong>senior UX &amp; UI designer</strong>. Jedním z prvních úkolů, které jsem na této pozici řešil, byl celkový 
                                <strong>redesign firemních webových stránek</strong>. Původní web vznikl na přelomu let 2011/ 2012 a po <strong>technické</strong>, <strong>obsahové</strong> a <strong>vizuální stránce</strong> již <strong>přestal odpovídat aktuálním potřebám společnosti</strong>.</p>                  
                        </div>
                    </div>
                    <div class="margin-box">
                        <h2>Postup řešení a UX metody</h2>
                        <div class="opacity-box">
                <ul>
                    <li>Podílel jsem se na <strong>organizaci workshopu</strong> s vedením společnosti, na kterém byly definovány hlavní cíle redesignu</li>
                    <li>Vedl jsem <strong>hloubkové rozhovory</strong> s vedoucími jednotlivých oddělení (obchod, marketing, vývoj)</li>
                    <li>Definoval jsem <strong>klíčové persony</strong>
                        <ul>
                            <li><strong>Zahraniční zákaznici</strong> ze Skandinávie, německy mluvících zemí a Velké Británie</li>
                            <li>Tuzemští a zahraniční zákazníci z bankovního sektoru, telekomunikací, e-commerce, logistiky, leteckého a lékařského průmyslu</li>
                            <li><strong>Uchazeči o zaměstnání</strong> ve firmě a <strong>žadatelé o studium v programu CN University</strong></li>
                        </ul>
                    </li>
                    <li>Ve spolupráci s copywriterem jsem připravil <strong>obsahovou strategii</strong></li>
                    <li>Zpřehlednil jsem <strong>informační architekturu</strong>
                    <ul>
                    <li>Lépe jsem kategorizoval <strong>portfolio služeb a reference</strong></li>   
                    <li>Zdůraznil jsem na <strong>nabídku volných pracovních pozic</strong> a program CN University</li> 
                    <li>Omezil jsem počet jazykových mutací</li>
                    </ul>
                    </li>
                    <li>Navrhl jsem několik <strong>sad wireframů</strong> a testoval je na vybraných zaměstnancích a zákaznících</li>
                    <li>Kompletně jsem připravil <strong>grafický design webu</strong> včetně vizuálních úprav korporátní identity</li>
                    <li>Převedl jsem schválené a otestované grafické návrhy do <strong>responsivních HTML šablon</strong> a <strong>interaktivního prototypu</strong> (HTML5, CSS3, Less, Bootstrap)</li>
                    <li>
                    Nastavil jsem <strong>webovou analytiku</strong>
                    <ul>
                        <li>Nadefinoval jsem hlavní události v <strong>Google Analytics</strong></li>
                        <li>Nastavil jsem sledování uživatelů pomocí <strong>trackovacích nástrojů</strong></li>
                    </ul>    
                    </li>
                </ul>

 </div>
