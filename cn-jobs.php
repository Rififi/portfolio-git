                        <h2>Výsledek</h2>
                        <div class="opacity-box">
                      <p>Noví uchazeči o zaměstnání hodnotili koncept kampaně „<strong>Nastartuj kariéru v CN Group...</strong>“ téměř ve všech případech <strong>velmi pozitivně</strong>. Na microsite oceňovali zejména <strong>srozumitelně popsaný průběh výběrového řízení</strong> a jasně definovanou náplň pracovních pozic. Rovněž <strong>přiblížení firemní kultury hravou formou</strong> se setkalo s kladnými ohlasy.</p> 

                      <p><strong>Koncept superhrdinů byl pak též úspěšně hodnocen</strong> i mezi samotnými zaměstnanci firmy CN Group. Na podzim roku 2016 se náborový web <strong>probojoval až do finále soutěže o nejlepší kariérní stránky roku 2016</strong> pořádanou společností Recruitment Academy.</p>
